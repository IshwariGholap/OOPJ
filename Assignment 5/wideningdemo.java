package com.example;

public class wideningdemo {

	public static void main(String[] args) {
		
		
		int num = 45;
		
		long n1 = num;
		System.out.println(n1);
		
		float n2 = num;
		System.out.println(n2);
		
		double n3 = num;
		System.out.println(n3);
		
		
		float num1 = 12.4f;
		
		double n4 = num1;
		System.out.println(n4);
		
		char ch = 'a';
		
		int ch2 = ch;
		System.out.println(ch2);
       
	}

}
